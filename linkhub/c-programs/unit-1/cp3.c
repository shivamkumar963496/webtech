
//WRITE A PROGRAM TO OVERLOAD INCREEMENT AND DECREMENT OPERATORS IN C
#include<stdio.h>
int main()
{
int x,y;
printf("ENTER THE VALUE OF X=");
scanf("%d",&x);
printf("ENTER THE VALUE OF Y=");
scanf("%d",&y);
x++;
y--;
printf("YOUR VALUE OF X=%d",x);
printf("\nYOUR VALUE OF Y=%d",y);

return 0;

}